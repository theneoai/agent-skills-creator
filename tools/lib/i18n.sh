#!/usr/bin/env bash
# i18n.sh - Bilingual strings (compatible with bash 3.2+)

# Translation function
t() {
    local key="$1"
    local lang="${LANG:-en}"
    local var_name="${lang^^}_${key}"
    local value="${!var_name}"
    if [[ -z "$value" ]]; then
        echo "$key"
    else
        echo "$value"
    fi
}

# Auto-detect language from environment
detect_lang() {
    local lang="${LANG:-en}"
    if [[ "$lang" == *"zh"* ]] || [[ "$lang" == *"CN"* ]]; then
        echo "zh"
    else
        echo "en"
    fi
}

